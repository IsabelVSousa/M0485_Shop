    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        String pName = productName.getText().trim();
        String pPrice = priceProduct.getText();

        switch (opc) {
            case 2:
                if (shop.findProduct(pName) != null) {
                    JOptionPane.showMessageDialog(this, "Ańadir producto incorrecto", "Este producto YA existe", JOptionPane.ERROR_MESSAGE);
                } else {
                    shop.addProduct(new Product(pName));
                    JOptionPane.showMessageDialog(this, "Ańadir producto correcto", "Se ha ańadido el producto con éxito", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
                break;
            case 3:
                int pStock = Integer.parseInt(productStock.getText());
                
                if (shop.findProduct(pName) != null) {
                    shop.addStock(pName, pStock);
                    JOptionPane.showMessageDialog(this, "Ańadir stock correcto", "Se ha ańadido el stock con éxito", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Ańadir stock incorrecto", "Este producto NO existe", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case 9:
                if (shop.findProduct(pName) != null) {
                    shop.deleteProduct(pName);
                    JOptionPane.showMessageDialog(this, "Eliminar producto correcto", "Se ha eliminado el producto con éxito", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Eliminar producto incorrecto", "Este producto NO existe", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
    }//GEN-LAST:event_okActionPerformed


